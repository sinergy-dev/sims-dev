    public function store_product(Request $req){
        $store = new ProductTag();
        $store->name_product = $req->name_product;
        $store->description_product = $req->desc_product;
        $store->date_add = Carbon::now()->toDateTimeString();
        $store->save(); 

        return redirect('sales/tag')->with('success', 'Product has been Added!');
    }

    public function store_tech(Request $req){
